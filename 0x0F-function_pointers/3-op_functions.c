#include "3-calc.h"

/**
 * op_add - add operation
 *
 * @a: number 1
 * @b: number 2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - sub operation
 *
 * @a: number 1
 * @b: number 2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - mul operation
 *
 * @a: number 1
 * @b: number 2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - div operation
 *
 * @a: number 1
 * @b: number 2
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - mod operation
 *
 * @a: number 1
 * @b: number 2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
